<?php

namespace modava\pages\models;

use common\models\User;
use modava\pages\PagesModule;
use modava\pages\models\table\ProjectTable;
use yii\behaviors\BlameableBehavior;
use yii\behaviors\SluggableBehavior;
use common\helpers\MyHelper;
use yii\db\ActiveRecord;
use Yii;

/**
 * This is the model class for table "project".
 *
 * @property int $id
 * @property string $title
 * @property string $slug
 * @property string $image
 * @property string $description
 * @property string $content
 * @property array $tech
 * @property int $position
 * @property string $ads_pixel
 * @property string $ads_session
 * @property int $status
 * @property string $language
 * @property int $views
 * @property int $created_at
 * @property int $updated_at
 * @property int $created_by
 * @property int $updated_by
 *
 * @property User $createdBy
 * @property User $createdBy0
 */
class Project extends ProjectTable
{
    public $toastr_key = 'project';

    public function behaviors()
    {
        return array_merge(
            parent::behaviors(),
            [
                'slug' => [
                    'class' => SluggableBehavior::class,
                    'immutable' => false,
                    'ensureUnique' => true,
                    'value' => function () {
                        return MyHelper::createAlias($this->title);
                    }
                ],
                [
                    'class' => BlameableBehavior::class,
                    'createdByAttribute' => 'created_by',
                    'updatedByAttribute' => 'updated_by',
                ],
                'timestamp' => [
                    'class' => 'yii\behaviors\TimestampBehavior',
                    'preserveNonEmptyValues' => false,
                    'attributes' => [
                        ActiveRecord::EVENT_BEFORE_INSERT => ['created_at', 'updated_at'],
                        ActiveRecord::EVENT_BEFORE_UPDATE => ['updated_at'],
                    ],
                ],
            ]
        );
    }

    /**
     * {@inheritdoc}
     */
    public function rules()
    {
        return [
            [['title', 'slug'], 'required'],
            [['description', 'content', 'ads_pixel', 'ads_session'], 'string'],
            [['tech'], 'safe'],
            [['position', 'status', 'views', 'created_at', 'updated_at', 'created_by', 'updated_by'], 'integer'],
            [['title', 'slug', 'image'], 'string', 'max' => 255],
            [['language'], 'string', 'max' => 25],
            [['slug'], 'unique'],
            [['created_by'], 'exist', 'skipOnError' => true, 'targetClass' => User::class, 'targetAttribute' => ['created_by' => 'id']],
            [['created_by'], 'exist', 'skipOnError' => true, 'targetClass' => User::class, 'targetAttribute' => ['created_by' => 'id']],
        ];
    }

    /**
     * {@inheritdoc}
     */
    public function attributeLabels()
    {
        return [
            'id' => PagesModule::t('pages', 'ID'),
            'title' => PagesModule::t('pages', 'Title'),
            'slug' => PagesModule::t('pages', 'Slug'),
            'image' => PagesModule::t('pages', 'Image'),
            'description' => PagesModule::t('pages', 'Description'),
            'content' => PagesModule::t('pages', 'Content'),
            'tech' => PagesModule::t('pages', 'Tech'),
            'position' => PagesModule::t('pages', 'Position'),
            'ads_pixel' => PagesModule::t('pages', 'Ads Pixel'),
            'ads_session' => PagesModule::t('pages', 'Ads Session'),
            'status' => PagesModule::t('pages', 'Status'),
            'language' => PagesModule::t('pages', 'Language'),
            'views' => PagesModule::t('pages', 'Views'),
            'created_at' => PagesModule::t('pages', 'Created At'),
            'updated_at' => PagesModule::t('pages', 'Updated At'),
            'created_by' => PagesModule::t('pages', 'Created By'),
            'updated_by' => PagesModule::t('pages', 'Updated By'),
        ];
    }

    public function afterSave($insert, $changedAttributes)
    {
        $this->on(yii\db\BaseActiveRecord::EVENT_AFTER_INSERT, function (yii\db\AfterSaveEvent $e) {
            if ($this->position == null)
                $this->position = $this->primaryKey;
            $this->save();
        });
        parent::afterSave($insert, $changedAttributes); // TODO: Change the autogenerated stub
    }

    /**
     * Gets query for [[User]].
     *
     * @return \yii\db\ActiveQuery
     */
    public function getUserCreated()
    {
        return $this->hasOne(User::class, ['id' => 'created_by']);
    }

    /**
     * Gets query for [[User]].
     *
     * @return \yii\db\ActiveQuery
     */
    public function getUserUpdated()
    {
        return $this->hasOne(User::class, ['id' => 'updated_by']);
    }
}
